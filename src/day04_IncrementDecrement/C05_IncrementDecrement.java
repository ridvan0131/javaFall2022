package day04_IncrementDecrement;

public class C05_IncrementDecrement {
    public static void main(String[] args) {
        int sayi1=10;
        System.out.println(sayi1+=5);
        System.out.println(sayi1);

        System.out.println(sayi1+12);
        System.out.println(sayi1);

        sayi1++;
        System.out.println(sayi1);


    }
}

package day16_forLoops;

public class C01_ForLoops {
    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            System.out.print(i+" ");//4 3 2 veriyor

        }

        for (int i = 0; i < 10; i--) {
            System.out.println(i+" ");

        }
        }
    }


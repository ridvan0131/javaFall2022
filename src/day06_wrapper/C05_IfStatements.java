package day06_wrapper;

public class C05_IfStatements {
    public static void main(String[] args) {
        int a=3;
        int b=5;

        if (a!=5){
            System.out.println("a sayisi 5'e esit degildir");
        }
        if (b<=7){
            System.out.println("b sayisi 7'den kucuk esittir");
        }
        if (a+b!=8){
            System.out.println("a ve b sayilarinin toplami 8 eder");
        }
    }
}
